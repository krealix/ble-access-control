def set_cell(cell, text):
    p = cell.paragraphs[0]
    if p.runs:
        p.runs[0].text = text
        for r in p.runs[1:]:
            r.text = ""
    else:
        p.add_run(text)


d = Document(TPL)
tbl = d.tables[0]
# r0 — заголовок; этапы пишем с r1.
for i, (name, due) in enumerate(STAGES, start=1):
    if i >= len(tbl.rows):
        break
    set_cell(tbl.rows[i].cells[0], name)  # наименование раздела
    set_cell(tbl.rows[i].cells[1], due)   # плановый срок
    # колонки 2 (факт), 3 (отметка), 4 (подпись) — заполняет руководитель.

d.save(OUT)
print("Календарный план сохранён:", OUT)
