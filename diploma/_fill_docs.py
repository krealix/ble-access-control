def is_line(s):
    s = s.strip()
    return bool(s) and set(s) <= LINE


def set_text(p, s):
    if p.runs:
        p.runs[0].text = s
        for r in p.runs[1:]:
            r.text = ""
    else:
        p.add_run(s)


def remove_p(p):
    p._element.getparent().remove(p._element)


