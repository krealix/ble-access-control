def fill_cell(cell, answer):
    lps = [p for p in cell.paragraphs if is_line(p.text)]
    if not lps:
        cell.add_paragraph(answer)
        return
    set_text(lps[0], answer)
    for p in lps[1:]:
        remove_p(p)


