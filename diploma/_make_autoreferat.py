ALIGN = {"j": AL.JUSTIFY, "c": AL.CENTER, "l": AL.LEFT, "r": AL.RIGHT}


def P(text="", a="j", indent=True, bold=False, italic=False, size=14, lead=None,
      before=0, after=0):
    p = doc.add_paragraph()
    pf = p.paragraph_format
    pf.alignment = ALIGN[a]
    pf.first_line_indent = Cm(1.25) if indent else Cm(0)
    pf.space_before = Pt(before)
    pf.space_after = Pt(after)
    if lead:
        r = p.add_run(lead)
        r.bold = True
        r.font.name = "Times New Roman"
        r.font.size = Pt(size)
    if text:
        r = p.add_run(text)
        r.bold, r.italic = bold, italic
        r.font.name = "Times New Roman"
        r.font.size = Pt(size)
    return p


