LINE = set("_ \t\n\r")


def is_line(s):
    s = s.strip()
    return bool(s) and set(s) <= LINE


def set_text(p, s):
    if p.runs:
        p.runs[0].text = s
        for r in p.runs[1:]:
            r.text = ""
    else:
        p.add_run(s)


def remove_p(p):
    p._element.getparent().remove(p._element)


def strip_lines(text):
    """Убрать подчёркивания и схлопнуть пробелы — оставить заголовок."""
    base = re.sub(r"_+", " ", text)
    return re.sub(r"\s+", " ", base).strip()


def fill_point(p, ans):
    set_text(p, strip_lines(p.text) + ": " + ans)


def fill_field(p, val):
    set_text(p, strip_lines(p.text) + " " + val)


def insert_after(p, text):
    new_el = deepcopy(p._element)
    p._element.addnext(new_el)
    np = Paragraph(new_el, p._parent)
    set_text(np, text)
    return np


