/// Алгоритм принятия решения о доступе по гистерезису зон сигнала.
///
/// Решение принимается не по мгновенному порогу RSSI и не по сглаженной оценке
/// (фильтр Калмана не используется), а по времени удержания метки в зоне
/// «близко» (A). Время измеряется числом подряд идущих замеров (счётчик A).
/// Доступ выдаётся при устойчивом удержании в зоне «близко» (A > Y); открытие
/// формируется ровно в момент набора порога, повторно — только после нового
/// набора удержания. Предварительный «взвод» «далеко» (B) больше не требуется:
/// метка, оказавшаяся рядом без подхода, тоже получает доступ. Счётчик A
/// накапливает измерения в зоне «близко» и обнуляется при выходе за нижний порог
/// (в зону «далеко»); в зоне нечувствительности между порогами счётчики
/// сохраняются. Счётчик «далеко» (B) ведётся для журнала и индикации ухода.
///
/// Пороги (RSSI, дБм): [nearRssi] (A) — выше = «близко»; [farRssi] (B) —
/// ниже = «далеко». [nearHoldY] (Y) — порог удержания «близко» (выдача доступа);
/// [farHoldX] (X) — порог удержания «далеко» (для журнала/индикации ухода).
class ZoneAccessAlgorithm {
  ZoneAccessAlgorithm({
    required this.nearRssi,
    required this.farRssi,
    required this.farHoldX,
    required this.nearHoldY,
  });

  /// A — порог «близко»: rssi > nearRssi.
  final int nearRssi;

  /// B — порог «далеко»: rssi < farRssi.
  final int farRssi;

  /// X — сколько подряд замеров «далеко» нужно для взвода (B > X).
  final int farHoldX;

  /// Y — сколько подряд замеров «близко» нужно для выдачи доступа (A > Y).
  final int nearHoldY;

  /// Состояние счётчиков по каждой метке (ключ — стабильный id метки).
  final Map<String, ZoneCounters> _state = {};

  /// Текущее число отслеживаемых меток.
  int get trackedCount => _state.length;

  /// Снимок счётчиков метки (или null, если метка не отслеживается).
  ZoneCounters? counters(String id) => _state[id];

  /// Обрабатывает одно измерение RSSI для метки [id]. Возвращает зону, счётчики
  /// и признак [ZoneSample.open] — нужно ли на этом замере открыть доступ.
  ZoneSample push(String id, int rssi) {
    final c = _state.putIfAbsent(id, ZoneCounters.new);
    Zone zone;
    var open = false;

    if (rssi < farRssi) {
      // Зона «далеко»: накапливаем пребывание (взвод). Счётчик «близко»
      // сбрасывается — метка не в зоне «близко».
      zone = Zone.far;
      c.b = (c.b ?? 0) + 1;
      c.a = 0;
    } else if (rssi > nearRssi) {
      // Зона «близко»: накапливаем ПОДРЯД идущее удержание. Доступ выдаётся по
      // удержанию «близко» (A > Y); предварительный «взвод» «далеко» не нужен.
      // Открытие формируется РОВНО в момент набора порога (A == Y+1) — это
      // естественная защёлка от повторных открытий: счётчик A обнуляется только
      // при уходе «далеко», поэтому до нового ухода порог снова не пересечётся.
      zone = Zone.near;
      c.a += 1;
      if (c.a == nearHoldY + 1) open = true;
    } else {
      // Зона нечувствительности: счётчики НЕ меняются. Счётчик A обнуляется
      // только при выходе за нижний порог (зона «далеко»), счётчик B —
      // сохраняется (память о взводе).
      zone = Zone.between;
    }

    return ZoneSample(zone: zone, a: c.a, b: c.b, open: open);
  }

  /// Метка пропала из зоны действия — удаляем её записи (как в спецификации).
  void remove(String id) => _state.remove(id);

  /// Полный сброс состояния (например, при изменении настроек).
  void clear() => _state.clear();
}

/// Зона сигнала относительно двух порогов.
enum Zone {
  far, // rssi < farRssi
  between, // гистерезис между порогами
  near, // rssi > nearRssi
}

extension ZoneName on Zone {
  String get label => switch (this) {
        Zone.far => 'далеко',
        Zone.between => 'между',
        Zone.near => 'близко',
      };
}

/// Счётчики пребывания метки в зонах.
class ZoneCounters {
  int a = 0; // пребывание «близко» (обнуляется только в зоне «далеко»)
  int? b; // пребывание «далеко»; null = метка ещё не была «далеко»
}

/// Результат обработки одного измерения.
class ZoneSample {
  ZoneSample({
    required this.zone,
    required this.a,
    required this.b,
    required this.open,
  });

  final Zone zone;
  final int a; // текущий счётчик «близко»
  final int? b; // текущий счётчик «далеко» (null — ещё не было «далеко»)
  final bool open; // фронт: на этом замере принято решение открыть
}
