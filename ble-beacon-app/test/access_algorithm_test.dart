import 'package:flutter_test/flutter_test.dart';
import 'package:ble_beacon_app/services/access_algorithm.dart';

void main() {
  // Параметры по умолчанию: «близко» rssi > -65, «далеко» rssi < -85,
  // Y = 3 (удержание «близко» для выдачи доступа), X = 3 (порог «далеко» —
  // на выдачу больше не влияет, ведётся для журнала/индикации ухода).
  ZoneAccessAlgorithm algo() => ZoneAccessAlgorithm(
        nearRssi: -65,
        farRssi: -85,
        farHoldX: 3,
        nearHoldY: 3,
      );

  /// Прогоняет последовательность RSSI и считает число открытий по метке [id].
  int feed(ZoneAccessAlgorithm a, String id, List<int> seq) {
    var opens = 0;
    for (final r in seq) {
      if (a.push(id, r).open) opens++;
    }
    return opens;
  }

  group('ZoneAccessAlgorithm', () {
    test('подход из «далеко» в «близко» открывает один раз', () {
      final a = algo();
      // Подход «далеко→близко»: набирает удержание «близко» (A>Y) → одно открытие.
      final opens = feed(a, 't1', [-90, -90, -90, -90, -60, -60, -60, -60]);
      expect(opens, 1);
    });

    test('повторных открытий при удержании «близко» нет', () {
      final a = algo();
      // После открытия метка остаётся «близко» — порог Y+1 уже пройден, повторно
      // не открывает (счётчик A не пересечёт порог без обнуления в «далеко»).
      final opens = feed(a, 't1', [
        -90, -90, -90, -90, // далеко
        -60, -60, -60, -60, -60, -60, -60, -60 // близко: открытие 1 раз
      ]);
      expect(opens, 1);
    });

    test('стабильно далёкая метка не открывает', () {
      final a = algo();
      expect(feed(a, 't1', List.filled(12, -92)), 0);
    });

    test('зона нечувствительности (между порогами) не накапливает', () {
      final a = algo();
      // Сигнал всё время между B и A — ни одного открытия.
      expect(feed(a, 't1', List.filled(12, -75)), 0);
    });

    test('счётчик A держится в зоне «между» (сброс только в «далеко»)', () {
      final a = algo();
      // близко(1,2) → между(A=2 держится) → близко(3,4) → открытие.
      expect(feed(a, 't1', [-90, -90, -90, -90, -60, -60, -75, -60, -60]), 1);
    });

    test('после ухода «далеко» возможно повторное открытие', () {
      final a = algo();
      var opens = 0;
      void run(List<int> seq) {
        for (final r in seq) {
          if (a.push('t1', r).open) opens++;
        }
      }

      run([-90, -90, -90, -90, -60, -60, -60, -60]); // открытие 1
      run([-90, -90, -90, -90]); // снова «далеко» → A обнуляется
      run([-60, -60, -60, -60]); // снова «близко» → открытие 2
      expect(opens, 2);
    });

    test('долгое «далеко» снимает накопленный счётчик «близко»', () {
      final a = algo();
      // Немного близко (A не достиг Y), затем «далеко» (A сбрасывается в 0),
      // затем снова близко — открытие происходит ровно один раз и только после
      // нового набора Y подряд.
      final opens = feed(a, 't1', [
        -60, -60, // A=2 (< Y+1) — открытия нет
        -90, -90, -90, -90, // далеко: A сброшен в 0
        -60, -60, -60, -60 // близко заново: A>Y → открытие
      ]);
      expect(opens, 1);
    });

    test('метка «близко» открывает по удержанию (взвод не требуется)', () {
      final a = algo();
      // Метка сразу «близко», без ухода «далеко»: набирает удержание Y → открытие
      // ровно один раз (защёлка до нового набора порога).
      expect(feed(a, 't1', [-60, -60, -60, -60, -60, -60]), 1);
    });

    test('удаление метки сбрасывает её счётчики', () {
      final a = algo();
      feed(a, 't1', [-60, -60]); // A=2 (< Y+1) — открытия ещё нет
      a.remove('t1');
      expect(a.counters('t1'), isNull);
      // Новая запись начинается с нуля: удержание набирается заново.
      expect(feed(a, 't1', [-60, -60]), 0); // A добрал лишь до 2 — рано
      expect(feed(a, 't1', [-60, -60]), 1); // A прошёл порог Y+1 → открытие
    });

    test('счётчики независимы для разных меток', () {
      final a = algo();
      var opens = 0;
      // Чередуем две метки — состояния не должны смешиваться.
      for (final r in [-90, -90, -90, -90]) {
        a.push('A', r);
        a.push('B', r);
      }
      for (final r in [-60, -60, -60, -60]) {
        if (a.push('A', r).open) opens++;
        if (a.push('B', r).open) opens++;
      }
      expect(opens, 2); // по одному открытию на каждую метку
    });
  });
}
